CLINICAL_PSYCHOLOGY = """
# CLINICAL PSYCHOLOGY & DIAGNOSIS

## Common Mental Health Disorders

### DEPRESSION (Major Depressive Disorder)
**Symptoms (2+ weeks):**
- Persistent sad/empty mood
- Loss of interest (anhedonia)
- Sleep changes (insomnia/hypersomnia)
- Appetite/weight changes
- Fatigue
- Worthlessness/guilt
- Concentration problems
- Suicidal thoughts

**Types:**
- Major depression
- Persistent depressive disorder (dysthymia)
- Seasonal affective disorder
- Postpartum depression
- Bipolar depression

**Treatment:**
- CBT (cognitive restructuring)
- Behavioral activation
- Interpersonal therapy (IPT)
- Antidepressants (SSRIs, SNRIs)
- Exercise, sleep hygiene, social connection

### ANXIETY DISORDERS
**Generalized Anxiety Disorder (GAD):**
- Excessive worry (6+ months)
- Restlessness
- Muscle tension
- Irritability
- Sleep problems

**Panic Disorder:**
- Recurrent panic attacks
- Fear of dying/going crazy
- Avoidance behavior
- Agoraphobia possible

**Social Anxiety:**
- Fear of judgment
- Avoidance of social situations
- Physical symptoms (blushing, sweating)

**Specific Phobias:**
- Intense fear of specific object/situation
- Avoidance
- Recognition fear is excessive

**Treatment:**
- CBT (exposure therapy)
- Relaxation techniques
- Mindfulness
- SSRIs, benzodiazepines (short-term)
- Graduated exposure

### TRAUMA & PTSD
**Symptoms (1+ month):**
- Intrusive memories/flashbacks
- Nightmares
- Avoidance of reminders
- Negative changes in thoughts/mood
- Hypervigilance
- Startle response

**Treatment:**
- Trauma-Focused CBT
- EMDR (Eye Movement Desensitization)
- Prolonged Exposure
- Somatic therapies
- Medication (SSRIs)

### BIPOLAR DISORDER
**Bipolar I:**
- Manic episodes (7+ days)
- Elevated/irritable mood
- Grandiosity
- Decreased sleep
- Racing thoughts
- Risky behavior
- Possible psychosis
- Depressive episodes

**Bipolar II:**
- Hypomania (less severe)
- Major depressive episodes

**Treatment:**
- Mood stabilizers (lithium)
- Antipsychotics
- Psychoeducation
- Sleep regulation crucial
- Therapy for maintenance

### PERSONALITY DISORDERS
**Cluster A (Odd/Eccentric):**
- Paranoid: Distrustful, suspicious
- Schizoid: Detached, solitary
- Schizotypal: Odd beliefs, social anxiety

**Cluster B (Dramatic/Emotional):**
- Borderline: Unstable relationships, self-image, emotions; fear of abandonment; self-harm
- Narcissistic: Grandiosity, need for admiration, lack of empathy
- Antisocial: Disregard for others, manipulation, lack of remorse
- Histrionic: Attention-seeking, dramatic

**Cluster C (Anxious/Fearful):**
- Avoidant: Social inhibition, feelings of inadequacy
- Dependent: Submissive, need to be taken care of
- Obsessive-Compulsive: Perfectionism, control, rigidity

**Treatment:**
- Dialectical Behavior Therapy (DBT) for Borderline
- Schema therapy
- Long-term psychotherapy
- Medication for symptoms (not cure)

### ADHD (Attention Deficit Hyperactivity Disorder)
**Inattentive Type:**
- Difficulty sustaining attention
- Careless mistakes
- Disorganized
- Loses things
- Easily distracted
- Forgetful

**Hyperactive-Impulsive Type:**
- Fidgets, restless
- Talks excessively
- Interrupts
- Difficulty waiting turn

**Combined Type:**
- Both inattentive and hyperactive symptoms

**Treatment:**
- Stimulant medication (methylphenidate, amphetamines)
- Behavioral therapy
- Organizational skills training
- Parent training (for children)
- School/work accommodations

### AUTISM SPECTRUM DISORDER
**Core Features:**
- Social communication deficits
- Restricted/repetitive behaviors
- Sensory sensitivities
- Need for routine
- Special interests

**Levels:**
- Level 1: Requiring support
- Level 2: Requiring substantial support
- Level 3: Requiring very substantial support

**Treatment:**
- Behavioral therapy (ABA - controversial)
- Speech therapy
- Occupational therapy
- Social skills training
- Educational support
- No medication for core symptoms

## Assessment & Diagnosis

### Clinical Interview
- Presenting problem
- History (medical, psychological, family)
- Mental status exam
- Risk assessment (suicide, self-harm, harm to others)

### Psychological Testing
- Intelligence (WAIS, WISC)
- Personality (MMPI, NEO-PI-R)
- Neuropsychological testing
- Projective tests (Rorschach - less used)

### Diagnostic Criteria
- DSM-5-TR (Diagnostic and Statistical Manual)
- ICD-11 (International Classification of Diseases)
- Cultural considerations
- Rule out medical causes
"""


