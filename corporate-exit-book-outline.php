    /**
     * PRINT THE OUTLINE
     */
    public function printOutline() {
        $meta = $this->bookMetadata();
        echo "=== {$meta['working_title']} ===\n";
        echo "{$meta['subtitle']}\n\n";
        echo "Target: {$meta['target_word_count']} | {$meta['estimated_pages']} pages\n";
        echo "Audience: {$meta['target_audience']}\n\n";
        
        $outline = $this->chapterOutline();
        foreach ($outline as $part) {
            echo "\n" . str_repeat("=", 60) . "\n";
            echo "{$part['part']}\n";
            echo "Theme: {$part['part_theme']}\n";
            echo str_repeat("=", 60) . "\n\n";
            
            foreach ($part['chapters'] as $ch) {
                echo "Chapter {$ch['chapter']}: {$ch['title']}\n";
                echo "{$ch['subtitle']}\n";
                echo "Words: {$ch['word_count']} | Faith element: {$ch['faith_element']}\n\n";
            }
        }
        
        echo "\n" . str_repeat("=", 60) . "\n";
        echo "SPECIAL FEATURES\n";
        echo str_repeat("=", 60) . "\n\n";
        
        $structure = $this->bookStructure();
        foreach ($structure['special_features'] as $feature) {
            echo "• $feature\n";
        }
        
        echo "\n\n" . str_repeat("=", 60) . "\n";
        echo "TIMELINE\n";
        echo str_repeat("=", 60) . "\n\n";
        
        $timeline = $this->timelineEstimate();
        echo "My role: {$timeline['my_role']}\n";
        echo "Your role: {$timeline['your_role']}\n\n";
        
        foreach ($timeline['estimated_timeline'] as $phase => $time) {
            echo "$phase: $time\n";
        }
        
        echo "\nIf rushed: {$timeline['if_rushed']}\n";
    }
