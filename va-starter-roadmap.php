    /**
     * Print the complete roadmap
     */
    public function printRoadmap() {
        $phases = [
            $this->phase1_DefineYourOffer(),
            $this->phase2_BuildPresence(),
            $this->phase3_GetFirstClients(),
            $this->phase4_DeliverAndDocument(),
            $this->phase5_ScaleAndRaisePrices(),
        ];
        
        foreach ($phases as $phase) {
            echo "\n" . str_repeat("=", 60) . "\n";
            echo "{$phase['title']}\n";
            echo "Duration: {$phase['duration']}\n";
            if (isset($phase['daily_target'])) {
                echo "Daily Target: {$phase['daily_target']}\n";
            }
            echo str_repeat("=", 60) . "\n\n";
            
            foreach ($phase['steps'] as $i => $step) {
                echo ($i + 1) . ". " . $step['task'] . "\n";
                echo str_repeat("-", 40) . "\n";
                foreach ($step['actions'] as $action) {
                    echo "   • $action\n";
                }
                echo "\n   DELIVERABLE: {$step['deliverable']}\n\n";
            }
        }
        
        echo "\n" . str_repeat("=", 60) . "\n";
        echo "DAILY TASK CHECKLIST\n";
        echo str_repeat("=", 60) . "\n\n";
        
        foreach ($this->getDailyChecklist() as $section => $tasks) {
            echo "$section\n";
            foreach ($tasks as $task) {
                echo "   [ ] $task\n";
            }
            echo "\n";
        }
        
        echo "\n" . str_repeat("=", 60) . "\n";
        echo "OUTREACH TEMPLATES\n";
        echo str_repeat("=", 60) . "\n\n";
        
        foreach ($this->getOutreachTemplates() as $name => $template) {
            echo "--- $name ---\n";
            echo $template . "\n\n";
        }
    }
