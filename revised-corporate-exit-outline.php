    /**
     * PRINT THE OUTLINE
     */
    public function printOutline() {
        $meta = $this->bookMetadata();
        echo "=== {$meta['working_title']} ===\n";
        echo "{$meta['subtitle']}\n\n";
        echo "Target: {$meta['target_word_count']} | {$meta['estimated_pages']} pages\n";
        echo "Tone: {$meta['tone']}\n";
        echo "Faith element: {$meta['faith_element']}\n\n";
        
        $outline = $this->chapterOutline();
        foreach ($outline as $part) {
            echo "\n" . str_repeat("=", 60) . "\n";
            echo "{$part['part']}\n";
            echo "Theme: {$part['part_theme']}\n";
            echo str_repeat("=", 60) . "\n\n";
            
            foreach ($part['chapters'] as $ch) {
                echo "Chapter {$ch['chapter']}: {$ch['title']}\n";
                echo "{$ch['subtitle']}\n";
                echo "Words: {$ch['word_count']}\n\n";
            }
        }
        
        echo "\n" . str_repeat("=", 60) . "\n";
        echo "FAITH PLACEMENT (Light Touch)\n";
        echo str_repeat("=", 60) . "\n\n";
        
        $faith = $this->faithPlacement();
        echo "Approach: {$faith['approach']}\n";
        echo "Frequency: {$faith['frequency']}\n";
        echo "Style: {$faith['style']}\n\n";
        
        echo "Examples:\n";
        foreach ($faith['examples'] as $ex) {
            echo "  • $ex\n";
        }
        
        echo "\n\n" . str_repeat("=", 60) . "\n";
        echo "PRACTICAL ELEMENTS\n";
        echo str_repeat("=", 60) . "\n\n";
        
        $practical = $this->practicalElements();
        echo "Throughout book:\n";
        foreach ($practical['throughout_book'] as $item) {
            echo "  • $item\n";
        }
        
        echo "\nTemplates included:\n";
        foreach ($practical['templates_included'] as $template) {
            echo "  • $template\n";
        }
    }
